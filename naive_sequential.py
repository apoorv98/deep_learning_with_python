#!/usr/bin/env python3

class NaiveSequential:
    def __init__(self, layers):
        self.layers = layers

    def __call__(self, inputs):
        x = inputs
        for layer in self.layers:
            x = layer(x)
        return x

    @property
    def weights(self):
        weights = []

        for layer in self.layers:
            weights += layer.weights
        return weights
